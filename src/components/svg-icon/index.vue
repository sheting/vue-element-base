<template>
  <svg :class="svgClass" aria-hidden="true">
    <use :xlink:href="iconName"/>
  </svg>
</template>

<script lang="ts">
import { Component, Vue, Prop } from 'vue-property-decorator'

@Component({
  name: 'svg-icon',
  components: {}
})
export default class SvgIconComponent extends Vue {
  @Prop({ required: true })
  iconClass!: string

  @Prop({ default: '' })
  className!: string
  
  // computed
  get iconName () {
    return `#icon-${this.iconClass}`
  }
  get svgClass () {
    if (this.className) {
      return 'svg-icon ' + this.className
    } else {
      return 'svg-icon'
    }
  }
}
</script>
<style scoped>
.svg-icon {
  width: 1em;
  height: 1em;
  vertical-align: -0.15em;
  fill: currentColor;
  overflow: hidden;
}
</style>
